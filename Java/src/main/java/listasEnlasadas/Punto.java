package listasEnlasadas;

public class Punto {
    private int columna, fila;

    public Punto(int columna, int fila) {
        this.columna = columna;
        this.fila = fila;
    }

    public int getColumna() {
        return columna;
    }

    public int getFila() {
        return fila;
    }
}
